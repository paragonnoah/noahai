import type { Finding } from "../../drizzle/schema";
import { invokeLLM } from "../_core/llm";

export interface ReportMetadata {
  title: string;
  severity: string;
  cvssScore: number;
  cweId?: string;
  affectedAssets: string[];
  discoveredDate: Date;
  reportedDate: Date;
}

/**
 * Generate HackerOne formatted report
 */
export async function generateHackerOneReport(finding: Finding, evidence: string[]): Promise<string> {
  const severity = finding.severity || "high";
  const cvssScore = parseFloat(finding.cvssScore || "0");

  const report = `# Vulnerability Report

## Summary
**Title:** ${finding.title}
**Severity:** ${severity.toUpperCase()}
**CVSS Score:** ${cvssScore}/10

## Description
${finding.description || "No description provided"}

## Impact
${finding.impactStatement || "This vulnerability could allow unauthorized access or data manipulation."}

## Reproduction Steps
${finding.reproductionSteps || `
1. Navigate to ${finding.affectedAssets?.[0] || "the affected endpoint"}
2. Perform the following action...
3. Observe the vulnerability
`}

## Proof of Concept
${evidence.join("\n\n") || "Evidence not available"}

## Affected Assets
${finding.affectedAssets?.map((asset) => `- ${asset}`).join("\n") || "- Unknown"}

## Remediation
Please refer to the OWASP guidelines for fixing ${finding.findingType} vulnerabilities.

## Timeline
- **Discovered:** ${finding.createdAt?.toISOString() || "Unknown"}
- **Reported:** ${new Date().toISOString()}

---
Report generated by ScopeHunter`;

  return report;
}

/**
 * Generate Bugcrowd formatted report
 */
export async function generateBugcrowdReport(finding: Finding, evidence: string[]): Promise<string> {
  const severityMap: Record<string, string> = {
    critical: "P1 - Critical",
    high: "P2 - High",
    medium: "P3 - Medium",
    low: "P4 - Low",
    info: "P5 - Informational",
  };

  const priority = severityMap[finding.severity || "high"] || "P3 - Medium";

  const report = `# ${finding.title}

**Priority:** ${priority}
**Vulnerability Type:** ${finding.findingType}
**CVSS v3.1 Score:** ${finding.cvssScore || "N/A"}

## Vulnerability Description
${finding.description || "No description provided"}

## Business Impact
${finding.impactStatement || "This vulnerability could impact the security of the application and its users."}

## Steps to Reproduce
${finding.reproductionSteps || `
1. Access the affected endpoint: ${finding.affectedAssets?.[0] || "Unknown"}
2. Perform the following actions...
3. Observe the vulnerability
`}

## Proof of Concept
${evidence.join("\n\n") || "Evidence not available"}

## Affected Endpoints/Assets
${finding.affectedAssets?.map((asset) => `- ${asset}`).join("\n") || "- Unknown"}

## Suggested Fix
Implement proper input validation and output encoding to prevent ${finding.findingType} vulnerabilities.

## References
- CWE: ${finding.cweId || "N/A"}
- OWASP: https://owasp.org/

---
Submitted via ScopeHunter`;

  return report;
}

