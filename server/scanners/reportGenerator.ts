/**
 * Generate JSON report for API submission
 */
export async function generateJSONReport(finding: Finding, evidence: string[]): Promise<string> {
  const report = {
    vulnerability: {
      id: finding.id,
      title: finding.title,
      type: finding.findingType,
      severity: finding.severity,
      cvssScore: parseFloat(finding.cvssScore || "0"),
      cweId: finding.cweId,
      description: finding.description,
      impactStatement: finding.impactStatement,
      reproductionSteps: finding.reproductionSteps,
      affectedAssets: finding.affectedAssets,
    },
    metadata: {
      status: finding.status,
      discoveredDate: finding.createdAt,
      reportedDate: new Date(),
      tags: finding.tags,
    },
    evidence: evidence,
  };

  return JSON.stringify(report, null, 2);
}

